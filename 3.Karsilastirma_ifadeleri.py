#---------------------------------Karşılaştırma İfadeleri----------------------------------#
a = 2
b = 3

print(a == b)    #a eşittir b mi?          Doğru mu yanlış mı?
print(a != b)    #a eşit değildir b mi?    Doğru mu yanlış mı?
print(a > b)     #a büyük b midir?         Doğru mu yanlış mı?
print(a < b)     #b büyük a mıdır?         Doğru mu yanlış mı?
print(a >= b)    #a büyük eşit b mi?       Doğru mu yanlış mı?
print(a <= b)    #b büyük eşit b mi?       Doğru mu yanlış mı?

#bu ifadelerin sonucu bool(true/false) olarak çıkmaktadır.