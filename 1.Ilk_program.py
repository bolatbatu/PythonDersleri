sayi_1 = 5
sayi_2 = 3

print(sayi_1+sayi_2)    #5 artı 3(8)
print(sayi_1-sayi_2)    #5 eksi 3(2)
print(sayi_1/sayi_2)    #5 bölü 3(1.66)
print(sayi_1*sayi_2)    #5 çarpı 3(15)
print(sayi_1%sayi_2)    #5 bölü 3 ten kalan(2)
print(sayi_1**sayi_2)   #5 üzeri 3(125)
print(sayi_1//sayi_2)   #5 bölü 3 ten tam bölen sayısı(1)
