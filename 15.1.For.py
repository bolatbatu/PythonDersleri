for i in range(4):                   #0'dan 4'e kadar olan sayıları listeler.
    print(i)                         #sonuc=0,1,2,3
for i in range(2,8):                 #2'den 8'e kadar olan sayıları listeler.
    print(i)                         #sonuc=2,3,4,5,6,7
for i in range(1,15,5):              #1'den başlayıp 15'e kadar 5er adım ile ilerler.
    print(i)                         #sonuc=1,6,11
